// --- Filesystem helpers ----------------------------------------------------

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function listFilesByExt(dir, ext) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((name) => name.endsWith(ext))
    .map((name) => path.join(dir, name));
}

function readText(file) {
  return fs.readFileSync(file, 'utf8');
}

function writeText(file, content) {
  ensureDir(path.dirname(file));
  fs.writeFileSync(file, content);
}

function fileExists(file) {
  try {
    return fs.statSync(file).isFile();
  } catch {
    return false;
  }
}

function dirExists(dir) {
  try {
    return fs.statSync(dir).isDirectory();
  } catch {
    return false;
  }
}

function fileContains(file, marker) {
  if (!fileExists(file)) return false;
  return readText(file).includes(marker);
}

function cleanupManagedFiles(destDir) {
  if (!dirExists(destDir)) return;
  const walk = (d) => {
    for (const entry of fs.readdirSync(d, { withFileTypes: true })) {
      const full = path.join(d, entry.name);
      if (entry.isDirectory()) {
        walk(full);
      } else if (entry.isFile() && fileContains(full, MANAGED_MARKER)) {
        fs.unlinkSync(full);
        console.log(`    Removed stale: ${full}`);
      }
    }
  };
  walk(destDir);
}

function copyFilesByExt(srcDir, ext, destDir, label) {
  ensureDir(destDir);
  cleanupManagedFiles(destDir);
  for (const src of listFilesByExt(srcDir, ext)) {
    const target = path.join(destDir, path.basename(src));
    fs.copyFileSync(src, target);
    console.log(`    ${(label + ':').padEnd(9)} ${target}`);
  }
}

function copyDirRecursive(srcDir, destDir) {
  ensureDir(destDir);
  for (const entry of fs.readdirSync(srcDir, { withFileTypes: true })) {
    const src = path.join(srcDir, entry.name);
    const dst = path.join(destDir, entry.name);
    if (entry.isDirectory()) {
      copyDirRecursive(src, dst);
    } else if (entry.isFile()) {
      fs.copyFileSync(src, dst);
    }
  }
}

function timestampSuffix() {
  const d = new Date();
  const pad = (n) => String(n).padStart(2, '0');
  return (
    d.getFullYear().toString() +
    pad(d.getMonth() + 1) +
    pad(d.getDate()) +
    pad(d.getHours()) +
    pad(d.getMinutes()) +
    pad(d.getSeconds())
  );
}

// --- Loader-header injection -----------------------------------------------

function buildLoaderBlock(rulesDir, title) {
  return (
    `${MARKER_BEGIN}\n` +
    `# ${title}\n\n` +
    `At the start of every conversation, preload these rule files (they define mandatory protocols):\n` +
    `1. \`${rulesDir}/00-init.md\` -- Global Operating System. Always active. Defines universal mandates that supersede individual expert personas. Don't skip this.\n` +
    `2. \`${rulesDir}/01-router.md\` -- MoE Router. Front-line triage. Analyzes intent and routes to the correct expert or pipeline.\n\n` +
    `These files establish the core protocol: routing before acting, logging all command output, labeling uncertainty (VERIFIED/HYPOTHESIS), and following the definition of done.\n` +
    `${MARKER_END}`
  );
}

function injectLoaderHeader(file, rulesDir, title) {
  const block = buildLoaderBlock(rulesDir, title);
  let content = '';
  if (fileExists(file)) {
    content = readText(file);
  } else {
    ensureDir(path.dirname(file));
  }

  let updated;
  if (content.includes(MARKER_BEGIN) && content.includes(MARKER_END)) {
    const start = content.indexOf(MARKER_BEGIN);
    let finish = content.indexOf(MARKER_END, start);
    finish += MARKER_END.length;
    if (finish < content.length && content[finish] === '\n') finish += 1;
    updated = content.slice(0, start) + block + '\n' + content.slice(finish);
  } else if (!content.trim()) {
    updated = block + '\n';
  } else {
    updated = block + '\n\n' + content;
  }
  fs.writeFileSync(file, updated);
  console.log(`    Loader:   ${file}`);
}

function replaceLoaderBlock(file, newBlock, label) {
  let content = '';
  if (fileExists(file)) {
    content = readText(file);
  } else {
    ensureDir(path.dirname(file));
  }
  let updated;
  if (content.includes(MARKER_BEGIN) && content.includes(MARKER_END)) {
    const start = content.indexOf(MARKER_BEGIN);
    let finish = content.indexOf(MARKER_END, start);
    finish += MARKER_END.length;
    if (finish < content.length && content[finish] === '\n') finish += 1;
    updated = content.slice(0, start) + newBlock + '\n' + content.slice(finish);
  } else if (!content.trim()) {
    updated = newBlock + '\n';
  } else {
    updated = newBlock + '\n\n' + content;
  }
  fs.writeFileSync(file, updated);
  console.log(`    ${label}: ${file}`);
}

