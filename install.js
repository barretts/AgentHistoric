#!/usr/bin/env node
/**
 * Unified install script for Agent Historic persona rules.
 *
 * Cross-platform Node.js implementation. Replaces install-local.sh and
 * install-local.ps1 with a single canonical entry point.
 *
 * Usage:
 *   node install.js [options]
 *
 * Options:
 *   --claude       Install rules for Claude Code
 *   --cursor       Install rules for Cursor
 *   --windsurf     Install rules for Windsurf
 *   --codex        Install rules for Codex
 *   --crush        Install rules for Crush
 *   --opencode     Install rules for OpenCode
 *   --gemini       Install rules for Gemini CLI
 *   --all          Install for all editors
 *   --list         List installed files and their status (no changes made)
 *   --scaffolded   Build with scaffolded voice (passed through to build script)
 *   --debug        Build with debug routing preamble (passed through to build script)
 *   -h, --help     Show this help
 *
 * If no flags are provided, the script auto-detects installed editors.
 */

