// --- Auto-detection --------------------------------------------------------

function detectEditors() {
  const detected = [];
  if (dirExists(path.join(HOME, '.claude'))) detected.push('claude');
  if (dirExists(path.join(HOME, '.cursor'))) detected.push('cursor');
  if (
    dirExists(path.join(HOME, '.windsurf')) ||
    dirExists(path.join(HOME, '.codeium', 'windsurf'))
  ) {
    detected.push('windsurf');
  }
  if (dirExists(path.join(HOME, '.codex'))) detected.push('codex');
  if (
    (APPDATA && dirExists(path.join(APPDATA, 'crush'))) ||
    dirExists(path.join(HOME, '.config', 'crush'))
  ) {
    detected.push('crush');
  }
  if (
    (APPDATA && dirExists(path.join(APPDATA, 'opencode'))) ||
    dirExists(path.join(HOME, '.config', 'opencode'))
  ) {
    detected.push('opencode');
  }
  if (dirExists(path.join(HOME, '.gemini'))) detected.push('gemini');
  return detected;
}

// --- CLI parsing -----------------------------------------------------------

function printHelp() {
  process.stdout.write(`Usage: node install.js [options]

Options:
  --claude       Install rules for Claude Code
  --cursor       Install rules for Cursor
  --windsurf     Install rules for Windsurf
  --codex        Install rules for Codex
  --crush        Install rules for Crush
  --opencode     Install rules for OpenCode
  --gemini       Install rules for Gemini CLI
  --all          Install for all editors
  --list         List installed files and their status (no changes made)
  --scaffolded   Build with scaffolded voice (externalized reasoning labels)
  --debug        Build with debug routing preamble visible
  -h, --help     Show this help

No flags = auto-detect installed editors.

Before running (if compiled output is missing), the script invokes:
  node scripts/build-prompt-system.mjs
`);
}

function parseArgs(argv) {
  const targets = [];
  const flags = { list: false, debug: false, scaffolded: false, help: false };
  for (const arg of argv) {
    switch (arg) {
      case '--claude': targets.push('claude'); break;
      case '--cursor': targets.push('cursor'); break;
      case '--windsurf': targets.push('windsurf'); break;
      case '--codex': targets.push('codex'); break;
      case '--crush': targets.push('crush'); break;
      case '--opencode': targets.push('opencode'); break;
      case '--gemini': targets.push('gemini'); break;
      case '--all':
        targets.push('claude', 'cursor', 'windsurf', 'codex', 'crush', 'opencode', 'gemini');
        break;
      case '--list': flags.list = true; break;
      case '--debug': flags.debug = true; break;
      case '--scaffolded': flags.scaffolded = true; break;
      case '-h':
      case '--help': flags.help = true; break;
      default:
        console.error(`Unknown option: ${arg}`);
        console.error('Run: node install.js --help');
        process.exit(1);
    }
  }
  return { targets, flags };
}

// --- Build orchestration ---------------------------------------------------

function maybeBuildPromptSystem(flags) {
  const buildScript = path.join(repoDir, 'scripts', 'build-prompt-system.mjs');
  const compiledReady =
    dirExists(SRC.claude) && listFilesByExt(SRC.claude, '.md').length > 0;

  const customFlags = flags.debug || flags.scaffolded;

  if (customFlags && fileExists(buildScript)) {
    console.log('--> Rebuilding prompt system (custom flags)...');
    const args = [buildScript];
    if (flags.debug) args.push('--debug');
    if (flags.scaffolded) args.push('--scaffolded');
    runNode(args);
  } else if (compiledReady) {
    console.log('--> Compiled files found, skipping build.');
  } else if (fileExists(buildScript)) {
    console.log('--> Building prompt system...');
    runNode([buildScript, '--no-debug']);
  } else {
    console.error('ERROR: Generated files not found and no build script available.');
    process.exit(1);
  }
  console.log('');
}

function runNode(args) {
  const result = spawnSync(process.execPath, args, {
    stdio: 'inherit',
    cwd: repoDir,
  });
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

