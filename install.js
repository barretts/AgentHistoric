// --- Main ------------------------------------------------------------------

function main(argv) {
  const { targets, flags } = parseArgs(argv);

  if (flags.help) {
    printHelp();
    return;
  }

  // Build (or skip) before any install/list work.
  maybeBuildPromptSystem(flags);

  let resolvedTargets = targets.length ? Array.from(new Set(targets)) : detectEditors();

  if (resolvedTargets.length === 0) {
    console.error(
      'ERROR: No supported editors detected. Use --claude, --cursor, --windsurf, --codex, --crush, --opencode, or --gemini.'
    );
    process.exit(1);
  }

  if (flags.list) {
    console.log('==> agent-historic installed files');
    console.log(`    Targets: ${resolvedTargets.join(' ')}`);
    console.log('');
    for (const t of resolvedTargets) {
      LISTERS[t]();
      console.log('');
    }
    console.log('==> No files were modified.');
    return;
  }

  console.log('==> agent-historic persona install');
  console.log(`    Source:  ${repoDir}`);
  console.log(`    Targets: ${resolvedTargets.join(' ')}`);
  console.log('    Style:   rich (universal default)');
  console.log('');
  console.log('--> Installing rules...');
  for (const t of resolvedTargets) {
    INSTALLERS[t]();
  }
  console.log('');
  console.log('==> Done.');

  printPostInstall(resolvedTargets, flags);
}

main(process.argv.slice(2));
