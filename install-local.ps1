$geminiMd = Join-Path $homeDir ".gemini/GEMINI.md"

$managedMarker = "managed_by: agent-historic"
$markerBegin = "<!-- agent-historic:loader-begin -->"
$markerEnd = "<!-- agent-historic:loader-end -->"

function Ensure-Dir([string]$path) {
  if (-not (Test-Path $path)) {
    New-Item -ItemType Directory -Path $path -Force | Out-Null
  }
}

function Cleanup-ManagedFiles([string]$destDir) {
  if (-not (Test-Path $destDir)) { return }
  Get-ChildItem -Path $destDir -Recurse -File -ErrorAction SilentlyContinue | ForEach-Object {
    $f = $_.FullName
    if (Select-String -Path $f -Pattern ([Regex]::Escape($managedMarker)) -Quiet -ErrorAction SilentlyContinue) {
      Remove-Item -Path $f -Force
      Write-Host "    Removed stale: $f"
    }
  }
}

function Inject-LoaderHeader([string]$file, [string]$rulesDir, [string]$title) {
  $block = @"
$markerBegin
# $title

At the start of every conversation, preload these rule files (they define mandatory protocols):
1. `$rulesDir/00-init.md` -- Global Operating System. Always active. Defines universal mandates that supersede individual expert personas. Don't skip this.
2. `$rulesDir/01-router.md` -- MoE Router. Front-line triage. Analyzes intent and routes to the correct expert or pipeline.

These files establish the core protocol: routing before acting, logging all command output, labeling uncertainty (VERIFIED/HYPOTHESIS), and following the definition of done.
$markerEnd
"@

  $content = ""
  if (Test-Path $file) {
    $content = Get-Content -Path $file -Raw
  } else {
    Ensure-Dir (Split-Path -Parent $file)
  }

  if ($content.Contains($markerBegin) -and $content.Contains($markerEnd)) {
    $start = $content.IndexOf($markerBegin)
    $finish = $content.IndexOf($markerEnd, $start)
    if ($start -ge 0 -and $finish -ge 0) {
      $finish = $finish + $markerEnd.Length
      if ($finish -lt $content.Length -and $content[$finish] -eq "`n") {
        $finish += 1
      }
      $updated = $content.Substring(0, $start) + $block + "`n" + $content.Substring($finish)
      Set-Content -Path $file -Value $updated -NoNewline
    }
  } elseif ([string]::IsNullOrWhiteSpace($content)) {
    Set-Content -Path $file -Value $block -NoNewline
  } else {
    Set-Content -Path $file -Value ($block + "`n`n" + $content) -NoNewline
  }

  Write-Host "    Loader:   $file"
}

function Copy-GlobToDest([string]$pattern, [string]$dest, [string]$label) {
  Ensure-Dir $dest
  Cleanup-ManagedFiles $dest
  $files = Get-ChildItem -Path $pattern -File -ErrorAction SilentlyContinue
  foreach ($f in $files) {
    Copy-Item -Path $f.FullName -Destination (Join-Path $dest $f.Name) -Force
    Write-Host ("    {0,-9} {1}" -f ($label + ":"), (Join-Path $dest $f.Name))
  }
}

function Install-Claude {
  Copy-GlobToDest (Join-Path $srcClaude "*.md") $destClaude "Claude"
  Inject-LoaderHeader (Join-Path $homeDir ".claude/CLAUDE.md") "~/.claude/rules" "Claude Code Instructions"
}

function Install-Cursor {
  Copy-GlobToDest (Join-Path $srcCursor "*.mdc") $destCursor "Cursor"
}

