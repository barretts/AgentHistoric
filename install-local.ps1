function Detect-Targets {
  $detected = @()
  if (Test-Path (Join-Path $homeDir ".claude")) { $detected += "claude" }
  if (Test-Path (Join-Path $homeDir ".cursor")) { $detected += "cursor" }
  if ((Test-Path (Join-Path $homeDir ".windsurf")) -or (Test-Path (Join-Path $homeDir ".codeium/windsurf"))) { $detected += "windsurf" }
  if (Test-Path (Join-Path $homeDir ".codex")) { $detected += "codex" }
  if ((Test-Path (Join-Path $appData "crush")) -or (Test-Path (Join-Path $homeDir ".config/crush"))) { $detected += "crush" }
  if ((Test-Path (Join-Path $appData "opencode")) -or (Test-Path (Join-Path $homeDir ".config/opencode"))) { $detected += "opencode" }
  if (Test-Path (Join-Path $homeDir ".gemini")) { $detected += "gemini" }
  return $detected
}

# Optional build step
$buildScript = Join-Path $repoDir "scripts/build-prompt-system.mjs"
if (Test-Path $buildScript) {
  Write-Host "--> Building prompt system..."
  $buildArgs = @($buildScript)
  if ($debug) { $buildArgs += "--debug" }
  if ($scaffolded) { $buildArgs += "--scaffolded" }
  Push-Location $repoDir
  try {
    & node @buildArgs
  }
  finally {
    Pop-Location
  }
  Write-Host ""
}

if (-not (Test-Path $srcClaude)) {
  throw "ERROR: Generated files not found and no build script available."
}

$targets = New-Object System.Collections.Generic.List[string]
if ($all) {
  @("claude", "cursor", "windsurf", "codex", "crush", "opencode", "gemini") | ForEach-Object { [void]$targets.Add($_) }
} else {
  if ($claude) { [void]$targets.Add("claude") }
  if ($cursor) { [void]$targets.Add("cursor") }
  if ($windsurf) { [void]$targets.Add("windsurf") }
  if ($codex) { [void]$targets.Add("codex") }
  if ($crush) { [void]$targets.Add("crush") }
  if ($opencode) { [void]$targets.Add("opencode") }
  if ($gemini) { [void]$targets.Add("gemini") }
}

if ($targets.Count -eq 0) {
  Detect-Targets | ForEach-Object { [void]$targets.Add($_) }
}

if ($targets.Count -eq 0) {
  throw "ERROR: No supported editors detected. Use --claude, --cursor, --windsurf, --codex, --crush, --opencode, or --gemini."
}

if ($list) {
  Write-Host "==> agent-historic installed files"
  Write-Host "    Targets: $($targets -join ' ')"
  Write-Host ""
  foreach ($t in $targets) {
    switch ($t) {
      "claude" { List-Claude }
      "cursor" { List-Cursor }
      "windsurf" { List-Windsurf }
      "codex" { List-Codex }
      "crush" { List-Crush }
      "opencode" { List-OpenCode }
      "gemini" { List-Gemini }
    }
    Write-Host ""
  }
  Write-Host "==> No files were modified."
  exit 0
}

Write-Host "==> agent-historic persona install"
