#!/usr/bin/env node
// Extract expert bios from src/site/experts-source/<slug>.html
// into src/site/src/experts-data.generated.ts.
// Source HTML files are the canonical content.

import {readdirSync, readFileSync, writeFileSync, mkdirSync} from 'node:fs';
import {dirname, join, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const srcDir = join(root, 'experts-source');
const outFile = join(root, 'src', 'experts-data.generated.ts');

function pick(html, re, group = 1) {
  const m = html.match(re);
  return m ? m[group].trim() : '';
}

function extract(html) {
  const full = pick(html, /<span class="pill">([^<]+)<\/span>/);
  const headshot = pick(html, /class="bio-headshot"[^>]*src="([^"]+)"/);
  const name = pick(html, /<section class="hero">[\s\S]*?<h1>([^<]+)<\/h1>/);
  const role = pick(html, /<section class="hero">[\s\S]*?<h1>[^<]+<\/h1>\s*<p>([^<]+)<\/p>/);
  const body = pick(html, /<article class="panel markdown-content">([\s\S]*?)<\/article>/);
  const aside = pick(html, /<aside class="panel">([\s\S]*?)<\/aside>/);
  const grounding = pick(aside, /<strong>Grounding:<\/strong>\s*([^<]+)/);
  const bestFor = pick(aside, /<strong>Best For:<\/strong>\s*([^<]+)/);
  const worksWith = pick(aside, /<strong>Works With:<\/strong>\s*([^<]+)/);
  return {full: decode(full), name: decode(name), role: decode(role), headshot, body, grounding: decode(grounding), bestFor: decode(bestFor), worksWith: decode(worksWith)};
}

function decode(s) {
  return s.replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>').replace(/&#39;/g, "'").replace(/&quot;/g, '"');
}

const files = readdirSync(srcDir).filter((f) => f.endsWith('.html')).sort();
const entries = {};
for (const f of files) {
  const slug = f.replace(/\.html$/, '');
  const html = readFileSync(join(srcDir, f), 'utf8');
  entries[slug] = extract(html);
}

const banner = `// GENERATED by scripts/build-experts-data.mjs from src/site/experts-source/*.html.\n// Do not edit by hand. Edit the source HTML files and re-run.\n`;
const ts = `${banner}export type ExpertBio = {\n  full: string;\n  name: string;\n  role: string;\n  headshot: string;\n  body: string;\n  grounding: string;\n  bestFor: string;\n  worksWith: string;\n};\n\nexport const EXPERT_BIOS: Record<string, ExpertBio> = ${JSON.stringify(entries, null, 2)};\n\nexport const EXPERT_SLUGS = Object.keys(EXPERT_BIOS);\n`;

mkdirSync(dirname(outFile), {recursive: true});
writeFileSync(outFile, ts);
console.log(`wrote ${outFile} with ${files.length} experts`);
